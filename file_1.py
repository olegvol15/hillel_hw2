# Відкриття файлу для запису (створить файл або перезапише існуючий)
with open('user_input.txt', 'w') as file:
    while True:
        user_input = input("Введіть дані для запису в файл (порожній рядок для завершення): ")
        if user_input == "":
            break  # Виходимо з циклу, якщо користувач ввів порожній рядок
        file.write(user_input + '\n')  # Записуємо введений рядок в файл з переводом рядка

print("Дані були записані у файл 'user_input.txt'.")

